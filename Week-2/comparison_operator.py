# >, <, >=, <=, ==, !=

deger1 = 15
deger2 = 20

print("{} > {} : {}".format(deger1,deger2, deger1>deger2))
print("{} < {} : {}".format(deger1, deger2, deger1 < deger2))

print("{} == {} : {}".format(deger1, deger2, deger1 == deger2))
print("{} != {} : {}".format(deger1, deger2, deger1 != deger2))

print("{} <= {} : {}".format(deger1, deger2, deger1 <= deger2))
print("{} >= {} : {}".format(deger1, deger2, deger1 >= deger2))